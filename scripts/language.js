function applyLanguage(lang) {
    document.querySelectorAll("[data-i18n]").forEach((el) => {
        const key = el.getAttribute("data-i18n");
        if (translations[lang][key]) {
            el.textContent = translations[lang][key];
        }
    });

    const langSwitch = document.getElementById("lang-switch");
    if (langSwitch) {
        const isIndexPage = window.location.pathname.endsWith("index.html") || window.location.pathname === "/";

        const basePath = isIndexPage ? "images" : "../images";

        langSwitch.style.backgroundImage = lang === "fr"
            ? `url('${basePath}/flag-en.png')`
            : `url('${basePath}/flag-fr.png')`;
    }

    const boutonCV = document.getElementById("boutonCV");

    if (boutonCV) {
        // Pour le fran√ßais
        if (lang === "fr") {
            boutonCV.href = "../media/CV-Enzo-Jouve.pdf";
            boutonCV.setAttribute("download", "CV-Enzo-Jouve.pdf");
        }
        // Pour l'anglais
        else if (lang === "en") {
            boutonCV.href = "../media/CV-Enzo-Jouve-EN.pdf";
            boutonCV.setAttribute("download", "CV-Enzo-Jouve-EN.pdf");
        }
    }
}



let currentLang = localStorage.getItem("lang") || "fr";
applyLanguage(currentLang);

document.getElementById("lang-switch").addEventListener("click", () => {
    currentLang = currentLang === "fr" ? "en" : "fr";
    localStorage.setItem("lang", currentLang);
    applyLanguage(currentLang);
});
